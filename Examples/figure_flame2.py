#!/usr/bin/env python
"""
Figures of flaming aurora generated by HiST program
"""
from histfeas import Path,userinput
from sys import argv

def hist_figure(P):
    from histfeas.main_hist import doSim

    P['makeplot'] =['fwd','optim','png','h5']

    doSim(P,
          cmd = ' '.join(argv),
          verbose=0
          )



if __name__ == '__main__':
    P = userinput(ini='in/2cam_flame.ini')

    outdir = Path(P.outdir).expanduser()


    P['x1d'] = [1,1,1]
    P['vlim'] = {'p':[-1.5,4.5,90,300,5e7,8e8,5e7,2e9], 'j':[1e3,1.1e5, 1e3,8e5],
            'b':[0,1.5e3]}
    P['overrides'] = {'ell':P['ell']}

    if not P['load']:
        print('running HiSTfeas program -- will write png and h5 to {}'.format(P['outdir']))
        hist_figure(P)
#%%
    from histfeas.loadAnalyze import readresults,findxlsh5
    h5list,xlsfn = findxlsh5(P['outdir'])
    readresults(h5list,P)
